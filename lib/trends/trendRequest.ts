
//------------------------------------------------------------
// Trend Period
//------------------------------------------------------------

export type TrendPeriod =
  | 1
  | 7
  | 30
  | -1;

//------------------------------------------------------------
// Trend Parameters
//------------------------------------------------------------

export interface TrendParameters {

  temperature: boolean;

  bloodPressure: boolean;

  pulse: boolean;

  spo2: boolean;

}

//------------------------------------------------------------
// Trend Request
//------------------------------------------------------------

export interface TrendRequest {

  //----------------------------------------------------------
  // Patient Information
  //----------------------------------------------------------

  patientId: string;

  patientName: string;

  //----------------------------------------------------------
  // Time Period
  //----------------------------------------------------------

  period: TrendPeriod;

  //----------------------------------------------------------
  // Selected Parameters
  //----------------------------------------------------------

  parameters: TrendParameters;

}